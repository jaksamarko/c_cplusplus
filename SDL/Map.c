int translateValueX(float v) {
    return (int)(((v/360.)+.5)*screenW);
}
int translateValueY(float v) {
    return (int)(((-v/180.)+.5)*screenH);
}

void createPolygon(SDL_Renderer *render, float *in, Sint16 r, Sint16 g, Sint16 b) {
    Sint16 *xV,*yV;
    int i,n,p2,p=0;
    do {
        for(p2=p;in[p2+1]!=0&&in[p2+2]!=0;p2+=2);
        n=(p2-p)/2;
        xV=(Sint16*) malloc(sizeof(int16_t)*n);
        yV=(Sint16*) malloc(sizeof(int16_t)*n);
        for(i=0;i<n;i++) {
            xV[i]=translateValueX(in[p+i*2]);
            yV[i]=translateValueY(in[p+i*2+1]);
        }
        filledPolygonRGBA(render,xV,yV,n,r,g,b,255);
        aapolygonRGBA(render,xV,yV,n,0,0,0,255);
        free(xV);
        free(yV);
        p=p2+2;
    } while(in[p]!=-1&&in[p+1]!=-1);
}

